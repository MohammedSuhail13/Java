package org.datatype;

public class Personalinfo {
	public static void main(String[] args) {
		//char
		char intial = 'P';
		System.out.println("Intial="+intial);
		
		//string
		String name = "Mohammed Suhail";
		System.out.println("Name="+name);
		
		//string
		String email = "suhail9813@gmail.com";
		System.out.println("Email="+email);
		
		//byte
		byte age = 26;
		System.out.println("Age="+age);
		
		//short
		short year = 1998;
		System.out.println("Year="+year);
		
		//boolean
		boolean maritial = false;
		System.out.println("Maritail="+maritial);
		
		//int
		int pincode = 636109;
		System.out.println("Pincode="+pincode);
		
		//long
		long phone = 9043738608l;
		System.out.println("Phone="+phone);
		
		//float
		float weight = 80.5366f;
		System.out.println("Weight="+weight);
		
		//double
		double height = 160.987456;
		System.out.println("Height="+height);
		
		
		
		
	}

}
